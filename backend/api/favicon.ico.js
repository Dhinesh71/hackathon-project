export default function handler(req, res) {
  // Set the correct content type for favicon
  res.setHeader('Content-Type', 'image/x-icon');
  // Send a blank favicon (1x1 transparent PNG as ICO)
  const blankFavicon = Buffer.from([
    0x00,0x00,0x01,0x00,0x01,0x00,0x10,0x10,0x00,0x00,0x01,0x00,0x04,0x00,
    0x28,0x01,0x00,0x00,0x16,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,
    0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00
  ]);
  res.status(200).end(blankFavicon);
}
